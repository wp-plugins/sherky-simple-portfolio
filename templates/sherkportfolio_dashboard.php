			<h3>Installation</h3>
			<ol>
				<li>BACKUP everything before you install the plugin.</li>
				<li>Upload sherkportfolio directory to the `/wp-content/plugins/` directory</li>
				<li>Activate the plugin through the 'Plugins' menu in WordPress</li>	
				<li>Update the Permalinks on settings.</li>
				<li>At Dashboard, under Portfolio menu, add your personal and contact details.</li>
				<li>Add projects for your portfolio through the Add New Project submenu of Portfolio menu.</li>
				<li>Provide the details of the project and click submit.</li>
				<li>List of projects will be available for edit and delete under the Project form.</li>
				<li>Add shortcode to the Page you created where you wish your portfolio will be displayed using <b>[sherkyportfolio]</b></li>
			</ol>
			

			<h3>Display Portfolio using Shortcode</h3>
			<ol>
				<li>Create a page for your Portfolio.</li>
				<li>Add the shortcode <b>[sherkyportfolio]</b> to the content text editor and update the page.</li>
				<li>That's it!</li>
			</ol>
					
